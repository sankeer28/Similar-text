function generateVarieties() {
    var input = document.getElementById('input').value;
    var output = document.getElementById('output');
    output.innerHTML = '';
    var words = getVarieties(input);
    words.forEach(function(word) {
        var row = document.createElement('tr');
        var wordCell = document.createElement('td');
        wordCell.textContent = word;
        row.appendChild(wordCell);
        var copyButtonCell = document.createElement('td');
        var copyButton = document.createElement('button');
        copyButton.textContent = 'Copy';
        copyButton.onclick = function() {
            navigator.clipboard.writeText(word);
            copyButton.textContent = 'Copied!';
            setTimeout(function() {
                copyButton.textContent = 'Copy';
            }, 2000);
        };
        copyButtonCell.appendChild(copyButton);
        row.appendChild(copyButtonCell);
        output.appendChild(row);
    });
}



function getVarieties(word, maxCombinations = 1000) {
    var varieties = [''];
    for (var i = 0; i < word.length; i++) {
        var newVarieties = [];
        var confusableChars = confusables[word[i]] || [word[i]];
        confusableChars.forEach(function(confusableChar) {
            varieties.forEach(function(variety) {
                newVarieties.push(variety + confusableChar);
            });
        });
        while (newVarieties.length > maxCombinations) {
            newVarieties.splice(Math.floor(Math.random() * newVarieties.length), 1);
        }
        varieties = newVarieties;
    }
    return varieties;
}

